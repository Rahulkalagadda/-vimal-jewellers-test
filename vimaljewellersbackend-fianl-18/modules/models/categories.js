import { DataTypes } from "sequelize";

export default (sequelize) => {
  return sequelize.define(
    "categories",
    {
      name: {
        type: DataTypes.STRING,
        allowNull: false,
        unique: true,
      },
      slug: {
        type: DataTypes.STRING,
        allowNull: false,
        unique: true,
      },
      image: {
        type: DataTypes.STRING,
        allowNull: true,
      },
    },
    {
      tableName: "categories",
      paranoid: true,
      omitNull: true,
      freezeTableName: true,
    }
  );
};
